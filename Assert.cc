#include <stdio.h>
#include <stdlib.h>
#include "Assert.h"

//////////////////////////////////////////////////////////////////////
// Write out assertion error message
//////////////////////////////////////////////////////////////////////

int _ASSERT_FAILED (char *filename, int line_number, const char *error_msg){
  fprintf (stderr, "Assertion failed in file \"%s\", line %d: %s\n",
	   filename, line_number, error_msg);
  abort();
  return 0;
}

